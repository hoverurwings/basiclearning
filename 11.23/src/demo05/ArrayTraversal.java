package demo05;

public class ArrayTraversal {
    public static void main(String[] args) {
        int[] arrayA = {2, 3, 4, 5, 5, 62, 2523, 344, 34233, 4, 3, 454, 34, 3};

        int l = arrayA.length;
        for (int i = 0; i < l; i++) {
            System.out.println(arrayA[i]);
        }
    }
}
